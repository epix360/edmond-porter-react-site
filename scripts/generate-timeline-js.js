#!/usr/bin/env node

/**
 * Generate timeline-data.js from timeline JSON files
 * This script converts JSON timeline files to a JavaScript file
 * to avoid GitHub Pages routing issues while maintaining CMS connectivity
 */

const fs = require('fs');
const path = require('path');

const generateTimelineJS = () => {
  const timelineDir = path.join(__dirname, '../public/content/timeline');
  const outputPath = path.join(__dirname, '../public/timeline-data.txt');
  
  let timelineData = [];
  
  // Try to read timeline files
  try {
    if (fs.existsSync(timelineDir)) {
      const files = fs.readdirSync(timelineDir).filter(f => f.endsWith('.json'));
      
      // Sort files by year
      files.sort((a, b) => {
        const yearA = parseInt(a.replace('.json', ''));
        const yearB = parseInt(b.replace('.json', ''));
        return yearA - yearB;
      });
      
      // Read each timeline file
      files.forEach(file => {
        const filePath = path.join(timelineDir, file);
        try {
          const content = fs.readFileSync(filePath, 'utf8');
          const data = JSON.parse(content);
          timelineData.push(data);
        } catch (error) {
          console.warn(`Warning: Could not read or parse ${file}:`, error.message);
        }
      });
    }
  } catch (error) {
    console.warn('Warning: Could not read timeline directory:', error.message);
  }
  
  // Fallback data if no files found
  if (timelineData.length === 0) {
    timelineData = [
      {
        "year": "2014",
        "milestone1_title": "The First Spark",
        "milestone1_description": "Publication of 'Whispers in the Grain' in a leading literary journal, marking his professional debut."
      },
      {
        "year": "2017",
        "milestone1_title": "Crossing the Continent",
        "milestone1_description": "Relocated to the Olympic Peninsula. The rugged landscapes began to heavily influence his work."
      },
      {
        "year": "2021",
        "milestone1_title": "A Breakthrough Release",
        "milestone1_description": "Debut novel 'The Archivist's Daughter' is released to critical acclaim.",
        "milestone2_title": "Literary Award",
        "milestone2_description": "Received recognition for outstanding contribution to contemporary literature."
      },
      {
        "year": "2026",
        "milestone1_title": "First novel published",
        "milestone1_description": "Turbulent Waters releases June 1, 2026"
      }
    ];
  }
  
  // Generate JavaScript file
  const jsContent = `// Timeline data served as JavaScript to avoid GitHub Pages routing issues
// This file is automatically generated from timeline JSON files
// Generated: ${new Date().toISOString()}

window.timelineData = ${JSON.stringify(timelineData, null, 2)};
`;
  
  fs.writeFileSync(outputPath, jsContent);
  console.log(`✅ Generated timeline-data.js with ${timelineData.length} entries`);
  
  return timelineData;
};

// Run the function
if (require.main === module) {
  generateTimelineJS();
}

module.exports = { generateTimelineJS };
